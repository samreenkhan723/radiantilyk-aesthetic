## 1. Nature of the Medication

Retatrutide (LY3437943) is an investigational triple-agonist peptide acting on **GLP-1**, **GIP**, and **glucagon** receptors. Administered by **weekly subcutaneous injection**. Phase 2 trials reported average weight reductions of up to ~24% at 48 weeks at the highest doses.

## 2. Reasonably Foreseeable Risks, Side Effects & Complications

I have been informed that retatrutide may cause, but is not limited to:

- **Gastrointestinal:** nausea, vomiting, diarrhea, constipation, abdominal pain, dyspepsia, GERD, gastroparesis, ileus, intestinal obstruction.
- **Pancreatic:** acute pancreatitis (potentially severe or fatal).
- **Gallbladder:** cholelithiasis, cholecystitis, biliary disease that may require surgery.
- **Renal:** acute kidney injury, especially from dehydration due to vomiting/diarrhea.
- **Endocrine/Metabolic:** hypoglycemia (particularly if combined with insulin or sulfonylureas), changes in heart rate, increases in blood pressure.
- **Thyroid:** as with other GLP-1 receptor agonists, animal studies of related compounds have shown thyroid C-cell tumors including medullary thyroid carcinoma (MTC). **Contraindicated in personal/family history of MTC or MEN 2.**
- **Psychiatric:** mood changes, anxiety, depression, suicidal ideation — discontinue and seek care immediately if these occur.
- **Hypersensitivity:** anaphylaxis, angioedema, injection-site reactions.
- **Diabetic retinopathy** worsening in susceptible patients.
- **Loss of lean body mass, sarcopenia, hair loss, facial volume loss ("Ozempic face"), skin laxity.**
- **Anesthesia risk:** retained gastric contents and aspiration during surgery/sedation — I will disclose retatrutide use to any surgeon/anesthesiologist and follow ASA fasting guidance (typically hold ≥1 week pre-op).
- **Pregnancy/lactation:** unknown teratogenicity. Effective contraception required. **Discontinue ≥2 months before attempting pregnancy.**
- **Unknown long-term risks** including but not limited to malignancy, cardiovascular events, and effects not yet characterized in trials.

